
public class Monom {
	
	private float coeficient;
	private int grad;
	
	public Monom(float coeficient, int grad) {
		
		this.coeficient = coeficient;
		this.grad = grad;
		
	}
	
	public float getCoeficient() {
		
		return coeficient;
		
	}
	
	public void setCoeficient(int coeficient) {
		
		this.coeficient = coeficient;
		
	}
	
	public int getGrad() {
		
		return grad;
		
	}
	
	public void setGrad(int grad) {
		
		this.grad = grad;
		
	}
	
}
